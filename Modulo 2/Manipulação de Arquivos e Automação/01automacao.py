#omportar a biblioteca pyautogui 
import pyautogui

#pressa é um comando que utilizamos para pressionar a tecla desajada 
pyautogui.press("Win")# vai pressianar a tecla do windows

# 'sleep' comando utilizado para deixar o codigo em espera por alguns segundos 
pyautogui.sleep(1)# eixa o codigo em espera por 1 segundo
#'write' é um comando utilizao para passar oque queremos escrever
pyautogui.write("youtube") #escreve google chrome 

pyautogui.press("Enter") #pressiona a tecla enter
